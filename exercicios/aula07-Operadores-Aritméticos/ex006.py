# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

n = int(input("Digite um número qualquer: ")) # Pede para o usuário digitar um número qualquer e armazena na variável n

print(f"O número digitado foi {n}. \nO seu dobro é {n * 2}. \no seu triplo é {n * 3} e a sua raiz quadradra é {n ** (1/2):.2f}.") # Mostra na tela o número digitado pelo usuário, o seu dobro, triplo e raiz quadrada