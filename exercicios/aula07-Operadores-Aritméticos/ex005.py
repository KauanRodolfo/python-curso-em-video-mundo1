# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor.

n = int(input("Digite um número inteiro: ")) # Pede para o usuário digitar um número inteiro e armazena na variável n

print(f"O número digitado foi {n}. O seu antecessor é {n - 1} e o seu sucessor é {n + 1}.") # Mostra na tela o número digitado pelo usuário, o seu antecessor e o seu sucessor
