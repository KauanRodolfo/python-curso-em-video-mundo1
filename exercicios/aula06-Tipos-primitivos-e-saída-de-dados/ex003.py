#Crie um programa que leia dois números e mostre a soma entre eles.

n1 = int(input("Digite um número: ")) # Pede para o usuário digitar um número e armazena na variável n1
n2 = int(input("Digite outro número: ")) # Pede para o usuário digitar outro número e armazena na variável n2
soma = (n1 + n2) # Calcula a soma dos dois números e armazena na variável soma
print(f"A soma entre {n1} e {n2} é igual a {soma}") # Mostra na tela a soma dos dois números digitados pelo usuário
